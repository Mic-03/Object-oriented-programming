public class enhanceif {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        // Enhanced if statement (ternary operator)
        int max = (x > y) ? x : y;

        System.out.println("Nilai maksimum adalah: " + max);
    }
}
// Enhanced if statement cocok digunakan ketika Anda memiliki ekspresi sederhana yang ingin Anda evaluasi berdasarkan kondisi tertentu dan ingin menulis kode dengan lebih ringkas.