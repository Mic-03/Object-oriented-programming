public class Forloop {
    public static void main(String[] args) {
        // Contoh for loop untuk mencetak angka 1 hingga 5
        for (int i = 1; i <= 5; i++) {
            System.out.println("Angka: " + i);
        }
    }
}
// for loop diatas akan mengeksekusi kode didalamnya mencetak nilai i sebanyak lima kali, mulai dari 1 sampai 5, karena loop harus berjalan selama nilai i kurang dari atau sama dengan 5.