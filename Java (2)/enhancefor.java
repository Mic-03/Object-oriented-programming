public class enhancefor {
    public static void main(String[] args) {
        // Array of numbers
        int[] numbers = {1, 2, 3, 4, 5};
        // Enhanced for loop to print each number
        for (int number : numbers) {
            System.out.println("Angka: " + number);
        }
    }
}
// Enhanced for loop digunakan diatas untuk mengiterasi melalui setiap elemen dalam array secara langsung tanpa perlu mengakses indeksnya secara eksplisit. Dengan ini kode jadi lebih ringkas.
// Fungsi enhanced for loop adalah statement yang berguna dalam Java untuk mengiterasi melalui elemen-elemen dalam array atau koleksi tanpa perlu mengakses indeksnya secara eksplisit.